            % Retrieve channel hierarchy
            ChannelData = bst_figures('GetFigureHandleField', hFig, 'ChannelData');
            % 
            if (~isempty(ChannelData))
                SameRegion = ChannelData(mDataPair(1:end,1),1) == ChannelData(mDataPair(1:end,2),1);
