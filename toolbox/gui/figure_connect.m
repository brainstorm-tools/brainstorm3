function [B,x] = bspline_basismatrix(n,t,x)
    if nargin > 2
        B = zeros(numel(x),numel(t)-n);
        for j = 0 : numel(t)-n-1
            B(:,j+1) = bspline_basis(j,n,t,x);
        end
    else
        [b,x] = bspline_basis(0,n,t);
        B = zeros(numel(x),numel(t)-n);
        B(:,1) = b;
        for j = 1 : numel(t)-n-1
            B(:,j+1) = bspline_basis(j,n,t,x);
        end
    end
end


